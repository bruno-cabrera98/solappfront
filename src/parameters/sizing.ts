export const size: any = {
  mobileS: '320px',
  mobileM: '375px',
  mobileL: '425px',
  tablet: '768px',
  laptop: '1024px',
  laptopL: '1440px',
  desktop: '2560px',
};

export const sizeInt = Object.keys(size).reduce((result: any, key) => {
  // eslint-disable-next-line no-param-reassign
  result[key] = parseInt(size[key].slice(0, -2), 10);
  return result;
}, {});

